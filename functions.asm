			PUBLIC DNUM
			PUBLIC PRINTINT
codes		SEGMENT PARA 'kod'
			ASSUME CS:codes
			
DNUM	 	PROC FAR
			PUSH AX
			PUSH BX
			PUSH BP
			MOV BP, SP
			MOV AX, [BP+10]
			XOR BX,BX
			CMP AX,BX
			JNE FRST
			MOV [BP+10],BX
			JMP LST
FRST:		INC BX
			CMP AX,BX
			JNE SEC
			MOV [BP+10],BX
			JMP LST
SEC:		CMP AX,2
			JNE THRD
			MOV [BP+10],BX
			JMP LST
THRD:		CMP AX,3
			JL LST
			DEC AX
			PUSH AX
			CALL DNUM
			CALL DNUM
			POP BX
			DEC AX
			PUSH AX
			CALL DNUM
			INC AX
			SUB AX,[BP-2]
			PUSH AX
			CALL DNUM
			POP AX
			ADD AX,BX
			MOV [BP+10],AX
			POP AX
LST:		POP BP
			POP BX
			POP AX
			RETF
DNUM		ENDP

PRINTINT	PROC NEAR
			PUSH CX
			PUSH DX
			XOR DX,DX
			MOV CX,10
			CMP AX,0
			JGE CALC_DIGITS
			NEG AX
			PUSH AX
			MOV AL,'-'
			CALL PUTC
			POP AX
CALC_DIGITS:DIV CX
			ADD DX,'0'
			PUSH DX
			XOR DX,DX
			CMP AX,0
			JNE CALC_DIGITS
DISP_LOOP:	POP AX
			CMP AX,0
			JE END_D_LOOP
			CALL PUTC
			JMP DISP_LOOP
END_D_LOOP:	POP DX
			POP CX
			RET
PRINTINT	ENDP

PUTC		PROC NEAR
			PUSH AX
			PUSH DX
			MOV DL,AL
			MOV AH,2
			INT 21H
			POP DX
			POP AX
			RET
PUTC		ENDP
codes		ENDS
			END